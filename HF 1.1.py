# Hangulat ertelmezo
hangulat = input("Milyen a hangulata?")
if hangulat == "boldog":
    print("Örülök, hogy boldognak látom!")
elif hangulat == "ideges":
    print("Vegyen 3 mély levegőt!")
elif hangulat == "szomoru":
    print("Fel a fejjel!")
elif hangulat == "izgatott":
    print("Igyon egy pohár vizet!")
elif hangulat == "nyugodt":
    print("Örülök, hogy nyugodtnak látom!")
else:
    print("Nem tudom értelmezni a hangulatát.")
